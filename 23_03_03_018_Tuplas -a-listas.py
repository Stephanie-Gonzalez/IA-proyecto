#Tuple: convertir listas en tupla y viceversa

lista = ["Asada", "Pastor", "Lengua", "Chorizo", "Arrachera", "Quesadilla", "Vampiro"]


tupla = tuple(lista)

print(tupla)


tupla = ("Asada", "Pastor", "Lengua", "Chorizo", "Arrachera")


lista = tuple(tupla)

print(lista)